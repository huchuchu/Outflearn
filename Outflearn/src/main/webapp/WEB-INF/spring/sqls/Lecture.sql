CREATE TABLE CLASS_DATA(
CLASS_NUM NUMBER REFERENCES CLASS_INFO(CLASS_NUM) ON DELETE CASCADE NOT NULL,
DATA_SUBHEAD VARCHAR2(250) NOT NULL,
DATA_SQ NUMBER NOT NULL,
DATA_TITLE VARCHAR2(250) NOT NULL,
