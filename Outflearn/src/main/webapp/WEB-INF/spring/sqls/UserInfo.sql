DROP TABLE USER_INFO;

DROP SEQUENCE USER_INFO_SQ;

CREATE SEQUENCE USER_INFO_SQ;

CREATE TABLE USER_INFO(
USER_NUM NUMBER PRIMARY KEY,
USER_ID VARCHAR2(1000) NOT NULL UNIQUE,
USER_PW VARCHAR2(1000) NOT NULL,
USER_NICKNAME VARCHAR2(1000) NOT NULL, 
USER_AUTHORITY VARCHAR2(10) DEFAULT 'ROLE_USER' NOT NULL,  
USER_EMAIL VARCHAR2(100) NOT NULL UNIQUE,
USER_ENABLED NUMBER(1) DEFAULT 1,  
CONSTRAINT authority_chk CHECK (USER_AUTHORITY IN ('ROLE_USER','ROLE_ADMIN','ROLE_TUTOR')));

